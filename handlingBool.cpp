// In cpp bool data type takes values true(1) and false (0)
#include <iostream>
#include <string>
using namespace std;

int main() {
    bool isfun = true;
    bool issick = false;

    cout << isfun << endl; // outputs 1 since it is true
    cout << issick << endl; //outputs 0 since it is false

    //Boolean expressions

    int x = 10;
    int y = 23;

    cout << (x > y); //outputs 0 as it is false
}